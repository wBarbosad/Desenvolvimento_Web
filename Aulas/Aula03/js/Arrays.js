var listaNomes = ["Fulano", "Cilano", "Beltrano"]
console.log(listaNomes)

listaNomes.push("Erculano") // Adiciona um item na lista
console.log(listaNomes)

listaNomes.pop() //remove ultimo item da lista
console.log(listaNomes)

listaNomes.shift() // remove primeiro item da lista
console.log(listaNomes)

listaNomes.sort() // ordena a lista
console.log(listaNomes)

listaNomes.reverse()
console.log(listaNomes)

console.log("Tamanho da lista ", listaNomes.length) //Length é o tamanho em inglês